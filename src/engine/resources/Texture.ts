export class Texture {
    readonly u: number
    readonly v: number
    readonly width: number
    readonly height: number
    readonly reference: keyof typeof TexturesData

    constructor(reference: keyof typeof TexturesData, u: number, v: number, width: number = 1, height: number = 1, gridSize = 64) {
        this.u = u * gridSize;
        this.v = v * gridSize;
        this.width = width * gridSize;
        this.height = height * gridSize;
        this.reference = reference;
    }
}